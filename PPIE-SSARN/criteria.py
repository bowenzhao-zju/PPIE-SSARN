# GaussianL2 Loss
def _gaussianL2(img1, img2, window, window_size, channel, size_average = True):
    mu1 = F.conv2d(img1, window, padding = window_size//2, groups = channel)
    mu2 = F.conv2d(img2, window, padding = window_size//2, groups = channel)

    diff = mu1 - mu2

    if size_average:
        return (diff ** 2).mean()
    else:
        return (diff ** 2).mean(1).mean(1).mean(1)

